void main() {
  //return int divisor
  int a = 5, b = 64;
  int div = b ~/ a;
  print(div);

  //ternary operation
  String Color = "Red";
  String Result = Color == "Red" ? "Color is red" : "Unknown";
  print(Result);

  // conditional expression
  int? year;
  var resul = year ?? 2023;
  print(resul);
}
